#include "math.h"

/*Function definitions*/
int sum(int a, int b)
{
    return a + b;
}

int subtraction(int a, int b)
{
    return a - b;
}

long long int multiplication(int a, int b)
{
    return (long long int)a * b;
}

float division(int a, int b)
{
    return (float)a / b;
}
